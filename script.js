var progressBar = document.getElementById("progressBar");
var timeDisplay = document.getElementById("duration");
audio.addEventListener("timeupdate", function() {
    var currentTime = audio.currentTime;
    var duration = audio.duration;
    var progress = (currentTime / duration) * 100;
    progressBar.value = progress;


    var currentMinutes = Math.floor(currentTime / 60);
  var currentSeconds = Math.floor(currentTime % 60);
  var durationMinutes = Math.floor(duration / 60);
  var durationSeconds = Math.floor(duration % 60);
  timeDisplay.innerText = currentMinutes + ":" + (currentSeconds < 10 ? "0" : "") + currentSeconds + " / " +
                          durationMinutes + ":" + (durationSeconds < 10 ? "0" : "") + durationSeconds;
  });
  
  // Skip forward when the progress bar is clicked
  progressBar.addEventListener("click", function(e) {
    var progressBarRect = progressBar.getBoundingClientRect();
    var clickX = e.clientX - progressBarRect.left;
    var progressBarWidth = progressBarRect.width;
    var progress = (clickX / progressBarWidth) * 100;
    audio.currentTime = (progress / 100) * audio.duration;
  });


